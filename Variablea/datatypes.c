#include <stdio.h>

int main() {
    int a = 10;
    float b = 10.5;
    long long contactnumber = 9900077000;
    char gender = 'f';
    char name[] = "buela";  // use char array for string

    printf("a value is %d \n", a);
    printf("b value is %f \n", b);
    printf("contact number is %lld \n", contactnumber);
    printf("gender is %c \n", gender);
    printf("name is %s \n", name);
    
    return 0;
}
